# helloworld.py
# Comments start with the '#' character
# They are to comment on the code, describing what is going on
# Everything after the '#' is ignored to the end of the line

print("Hello, World!")
print('Hello again!')  # you can use either 'single quotes' or "double quotes"
print('Hello ' + 'there')  # you can concatenate strings (string of characters or text) together
print('I love Python! ' * 100)  # a quick way to multiply strings
print('Fred\'s Pie Shop')  # use a \ character to allow a string to include the enclosing ' character
